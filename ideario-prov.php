              <h5 class="accordion-header" id="heading4">
                <button class="accordion-button collapsed" type="button" data-bs-toggle="collapse" data-bs-target="#collapse4" aria-expanded="false" aria-controls="collapse4" id="marcoconceptual-faq">
                  LA FAMILIA                </button>
              </h5>
              <div id="collapse4" class="accordion-collapse collapse" aria-labelledby="heading4" data-bs-parent="#faqs">
                <div class="accordion-body">
                  <hr>
                  La familia entendida como la comunión de personas, reflejo e imagen de la comunión del Padre y del Hijo en el Espíritu Santo, cuya actividad procreadora y educativa es reflejo de la obra creadora de Dios (CEC, 2205).
                  La familia es el lugar natural en donde esta relación de reciprocidad y comunión entre el hombre y la mujer encuentra su plena actuación. La familia, como sociedad natural en la que se realizan plenamente la reciprocidad y la complementariedad entre el hombre y la mujer, precede al mismo orden sociopolítico del Estado, cuya libre actividad legislativa debe tenerlo en cuenta y darle el justo reconocimiento (LCVM, 36).
                  Primeros Educadores: puesto que los padres han dado la vida a los hijos, son ellos los primeros y principales educadores. La familia es la primera escuela de las virtudes sociales, de las que todas las sociedades necesitan. (Gravissimun educationis, 3).
                  Miembros de la Comunidad Educativa: le corresponde a los Padres enriquecer la comunión, en torno al proyecto educativo, haciendo vivo y explícito el clima familiar que debe caracterizar la comunidad educativa. La escuela, a su vez, se sitúa junto a la familia como un espacio comunitario, orgánico e intencional que acompaña su empeño educativo, según la lógica de la subsidiariedad. La relación escuela-familia se configura como plena participación de los padres en la vida de la comunidad educativa, no sólo debido a su primordial responsabilidad en la educación de los hijos, sino también en virtud del compartir la identidad y el proyecto que caracterizan la escuela católica y que ellos deben conocer y aprobar, con disponibilidad interior. (Educar juntos en la escuela católica, 48).
                  A la acción educativa de la familia se une la de la escuela, que interactúa de manera subsidiaria. Fortalecida por su fundación evangélica, «la escuela católica se configura como escuela para la persona y para las personas. “La persona de cada uno, en sus necesidades materiales y espirituales, es el centro del magisterio de Jesús: por esto el fin de la escuela católica es la promoción de la persona humana”. (LCVM, 39).
                  Es necesario que los padres cooperen estrechamente con los maestros de las escuelas, a quienes confían la educación de sus hijos; los profesores han de trabajar unidos con los padres (CIC, 796).                </div>
