              <h5 class="accordion-header" id="heading11">
                <button class="accordion-button collapsed" type="button" data-bs-toggle="collapse" data-bs-target="#collapse11" aria-expanded="false" aria-controls="collapse11" id="marcoconceptual-faq">
                  CONCEPCIÓN ANTROPOLÓGICA
                </button>
              </h5>
              <div id="collapse11" class="accordion-collapse collapse" aria-labelledby="heading11" data-bs-parent="#faqs">
                <div class="accordion-body">
                  <hr>
                  Nuestras familias eligen para sus hijos una educación católica y para eso exigen una institución católica coherente con su Fe. Estamos llamados a dialogar con concepciones culturales diversas y, hasta opuestas, sin por eso dejar de sostener aquello en que creemos. «Este diálogo, para ser auténtico, debe ser claro, evitando relativismos y sincretismos» (SS. Benedicto XVI, 7/1/2008). Toda educación depende de la concepción del “hombre” que se asuma y de su concepción antropológica. Aquello que define “católica” a una institución educativa es el hecho de vivir y referirse a la concepción cristiana de la realidad. «Jesucristo es el centro de tal concepción» (LEC, 33).
                  Entendemos al hombre como una unidad sustancial de cuerpo y alma. Esto nos lleva a atender tanto su corporalidad como su espíritu. A su vez, cuenta con dos potencias o capacidades propias: la inteligencia por la que tiende a la verdad y la voluntad por la que elige el bien. «En la unidad de cuerpo y alma, el hombre, por su misma condición corporal, es una síntesis del universo material, el cual alcanza por medio del hombre su más alta cima y alza la voz para la libre alabanza del Creador» (GS, 14).
                  Por lo tanto, la naturaleza humana, para superar cualquier fisicismo o naturalismo, debe entenderse a la luz de la unidad del alma y el cuerpo, «en la unidad de sus inclinaciones de orden espiritual y biológico, así como de todas las demás características específicas, necesarias para alcanzar su fin» (LCVM, 32).
                  En su carácter de creatura, es un ser en proceso de formación. Lo que lo hace abierto a crecer y perfeccionarse en todas sus dimensiones. Si bien la educación de los hijos es deber y derecho de los padres, la escuela católica los asiste en esta tarea. Por esto, debe tener en cuenta todos los aspectos del ser humano al elaborar sus planes de estudio y proyecto institucional.
                  La antropología cristiana tiene sus raíces en la narración de los orígenes tal como aparece en el Libro del Génesis, donde está escrito que «Dios creó al hombre a su imagen [...], varón y mujer los creó» (Gen 1, 27) (LCVM, 3). La visión antropológica cristiana ve en la sexualidad un elemento básico de la personalidad, un modo propio de ser, de manifestarse, de comunicarse con los demás, de sentir, de expresar y de vivir el amor humano. (LCVM, 4).
                  Es necesario reiterar la raíz metafísica de la diferencia sexual: de hecho, hombre y mujer son las dos formas en que se expresa y se realiza la realidad ontológica de la persona humana. El rechazo de esta dualidad presente en la llamada ideología de género, no solo borra la visión de la creación, sino que delinea una persona abstracta «que después elije para sí mismo, autónomamente, una u otra cosa como naturaleza suya. Se niega a hombres y mujeres su exigencia creacional de ser formas de la persona humana que se integran mutuamente.» (LCVM, 3).  Como en múltiples ocasiones señaló el Papa Francisco en comunión con todo el Magisterio, consideramos la ideología de género como incompatible con el pensamiento cristiano ( AL, 53, 56, 153, 155 y 251).
