                  CONCEPCIÓN PEDAGÓGICA                </button>
              </h5>
              <div id="collapse2" class="accordion-collapse collapse" aria-labelledby="heading2" data-bs-parent="#faqs">
                <div class="accordion-body">
                  <hr>
                  La educación religiosa escolar debe ser impartida “de manera explícita y sistemática”. Su contenido central es “enseñar la doctrina evangélica tal como es trasmitida por la Iglesia Católica” (LEC, 49).
                  El PI y el PC deben desarrollarse desde una cosmovisión cristiana, haciendo una lectura humana y creyente de la vida institucional y de la currícula y promoviendo una acción educativa inspirada en una concepción cristiana del hombre, de la vida y del mundo.                    </p>
