              <h5 class="accordion-header" id="heading3">
                <button class="accordion-button collapsed" type="button" data-bs-toggle="collapse" data-bs-target="#collapse3" aria-expanded="false" aria-controls="collapse3" id="marcoconceptual-faq">
                  LOS EDUCADORES                </button>
              </h5>
              <div id="collapse3" class="accordion-collapse collapse" aria-labelledby="heading3" data-bs-parent="#faqs">
                <div class="accordion-body">
                  <hr>
                  Los educadores de las escuelas Católicas del Obispado de Zárate Campana tienen la misión de ofrecer a los alumnos una educación integral y un acompañamiento en el descubrimiento de su libertad personal, don de Dios.
                  El educador, preparado en la propia disciplina, y dotado además de sabiduría cristiana, transmite al alumno el sentido profundo de lo mismo que enseña y lo conduce, trascendiendo las palabras, al corazón de la verdad total (LEC, 41). Debe orientar a sus estudiantes hacia la búsqueda de la verdad y el sentido de la existencia, hacia una construcción positiva de sí mismos y de la vida, en el horizonte de una formación integral. También deben enseñar y acompañar a los jóvenes a descubrir la belleza, lo justo y lo bueno. La eficacia de la acción colectiva del personal docente está dada por tener una visión de valores compartidos y ser una comunidad que aprende, no sólo que enseña.                </div>
