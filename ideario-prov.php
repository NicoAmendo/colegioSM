                  Las líneas de acción están determinadas por las siguientes dimensiones educativo-socio-pastorales:
                <br>
                <ul style="list-style: none;">
                  <li><i class="bi bi-check2-circle"></i> Comunión (Koinonia): el Reino de Dios vivido en fraternidad y comunión, promoviendo la identidad institucional y convergencia en la cosmovisión cristiana.</li>
                  <li><i class="bi bi-check2-circle"></i> Anuncio (Kerigma): el Reino de Dios proclamado en el anuncio liberador y significativo del Evangelio, involucrando a todos los actores.</li>
                  <li><i class="bi bi-check2-circle"></i> Celebración (Liturgia): el Reino de Dios celebrado en los ritos festivos de la liturgia y la vida de oración en la Comunidad Educativa.</li>
                  <li><i class="bi bi-check2-circle"></i>	Servicio: (Diakonia): el Reino de Dios realizado en el amor, la caridad y el servicio fraterno.</li>
                  <li><i class="bi bi-check2-circle"></i> Cultura: el Reino de Dios reflexionado críticamente a la luz del Evangelio con los valores de vida del pueblo y del patrimonio cultural de la humanidad.
                    La escuela se compromete a crear un ambiente físico y espiritual que nos ayude a vivir pastoralmente este Ideario a través de las siguientes pautas:
                    </li>
                  <li><i class="bi bi-check2-circle"></i> Centralidad de la presencia real de Cristo y de su Madre, la Virgen María.</li>
                  <li><i class="bi bi-check2-circle"></i>	Ambiente físico: presencia de signos religiosos.</li>
                  <li><i class="bi bi-check2-circle"></i>	Presencia del Capellán y labor como administrador de los Sacramentos.</li>
                  <li><i class="bi bi-check2-circle"></i> Vida sacramental como fuente de la vida escolar.</li>
                  <li><i class="bi bi-check2-circle"></i> Oración en la vida escolar, como el momento más solemne e importante de la actividad diaria.</li>
                  <li><i class="bi bi-check2-circle"></i> La disciplina desde la caridad evangélica y los mandamientos.</li>
                  <li><i class="bi bi-check2-circle"></i> La actividad apostólica como fruto de la vivencia propuesta: la escuela es misionera en su esencia.</li>
