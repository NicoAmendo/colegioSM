                <button class="accordion-button collapsed" type="button" data-bs-toggle="collapse" data-bs-target="#collapse6" aria-expanded="false" aria-controls="collapse6" id="marcoconceptual-faq">
                  LA ESCUELA CATÓLICA                </button>
              </h5>
              <div id="collapse6" class="accordion-collapse collapse" aria-labelledby="heading6" data-bs-parent="#faqs">
                <div class="accordion-body">
                  <hr>
                  La misión de la Iglesia es evangelizar; es decir, proclamar a todos, el gozoso anuncio de la salvación, engendrar con el bautismo nuevas creaturas en Cristo y de educarlas para que vivan conscientemente como hijos de Dios (LEC, 7). Para llevar a término esta misión, la Iglesia crea sus propias escuelas, porque reconoce en la escuela un medio privilegiado para la formación integral del hombre, en cuanto que ella es un centro donde se elabora y se transmite una concepción específica del mundo, del hombre y de la historia (LEC, 8).
                  La Escuela Católica supone no solamente una elección de valores culturales, sino también una elección de valores de vida que deben estar presentes de manera operante (LEC, 33) La Escuela Católica enseña a los jóvenes a interpretar la voz del universo que les revela al Creador y, a través de las conquistas de la ciencia, a conocer mejor a Dios y al hombre. En la vida diaria del ciclo escolar, el alumno aprende que, a través de su obrar en el mundo, él está llamado a ser un testimonio vivo del amor de Dios entre los hombres, porque él mismo forma parte de una historia de salvación que recibe su último sentido de Cristo salvador de todos los hombres (LEC, 46)
                  Consciente de que no basta ser regenerados por el bautismo, para ser cristianos, sino que es necesario vivir y obrar conforme al Evangelio, la Escuela Católica se esfuerza por crear en el ámbito de la comunidad escolar un clima que ayude al alumno a vivir su fe de una manera cada día más madura, y a adquirir gradualmente una actitud pronta para asumir las responsabilidades de su bautismo (LEC, 47).
